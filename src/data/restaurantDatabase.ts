export function getRestaurantsByCategory(categoryId: string): Restaurant[] {
  return RESTAURANTS.filter(r => r.category === categoryId)
}

export function searchRestaurants(query: string): Restaurant[] {
  const q = query.toLowerCase().trim()
  if (!q) return []

  return RESTAURANTS.filter(r => {
    if (r.name.toLowerCase().includes(q)) return true
    if (r.category.toLowerCase().includes(q)) return true
    if (r.summary.toLowerCase().includes(q)) return true
    if (r.safeItems.some(item => item.name.toLowerCase().includes(q))) return true
    return false
  }).sort((a, b) => {
    const aExact = a.name.toLowerCase().includes(q) ? 0 : 1
    const bExact = b.name.toLowerCase().includes(q) ? 0 : 1
    if (aExact !== bExact) return aExact - bExact
    const ratingOrder: Record<SafetyRating, number> = { great: 0, good: 1, limited: 2, avoid: 3 }
    return ratingOrder[a.rating] - ratingOrder[b.rating]
  })
}

